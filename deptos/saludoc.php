				        		<span class="font-weight-bold">Art. 5º</span> — Servicio de Medicina del Trabajo. El Servicio de Medicina del Trabajo tiene como misión fundamental promover y mantener el más alto nivel de salud de los trabajadores, debiendo ejecutar, entre otras, acciones de educación sanitaria, socorro, vacunación y estudios de ausentismo por morbilidad. Su función es esencialmente de carácter preventivo, sin perjuicio de la prestación de la asistencia inicial de las enfermedades presentadas durante el trabajo y de las emergencias médicas ocurridas en el establecimiento, hasta tanto se encuentre en condiciones de hacerse cargo el servicio médico que corresponda.<br>

								<span class="font-weight-bold">Art. 6º</span> — Los Servicios de Medicina del Trabajo deberán estar dirigidos por graduados universitarios especializados en Medicina del Trabajo con título de Médico del Trabajo.<br>
