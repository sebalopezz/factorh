				<p style="font-size: 2.6vh;">De acuerdo con la <a href="https://www.who.int/es/" target="_blank">ORGANIZACIÓN MUNDIAL DE LA SALUD</a>, la <span class="font-weight-bold">Salud Ocupacional</span> es una actividad multidisciplinaria dirigida a promover y proteger la salud de los trabajadores mediante la prevención y el control de enfermedades y accidentes y la eliminación de los factores y condiciones que ponen en peligro la salud y la seguridad en el trabajo. Además procura generar y promover el trabajo seguro y sano, así como buenos ambientes y organizaciones de trabajo realzando el bienestar físico mental y social de los trabajadores y respaldar el perfeccionamiento y el mantenimiento de su capacidad de trabajo. A la vez que busca habilitar a los trabajadores para que lleven vidas social y económicamente productivas y contribuyan efectivamente al desarrollo sostenible, la salud ocupacional permite su enriquecimiento humano y profesional en el trabajo.</p>
